from math import pi as PI   

'''
1. Define una función que calcule el área de un círculo dado su radio.
'''

''' *************************************************************************************** '''        
''' ************************************** FUNCIONES ************************************** '''    
''' *************************************************************************************** ''' 

''' ========================= Función calcularAreaCirculo (radio) ========================= '''
def calcularAreaCirculo (radio) :
    
    return (round(PI * (radio * radio), 2))



''' *************************************************************************************** '''        
''' *************************************** MAINAPP *************************************** '''    
''' *************************************************************************************** '''

assert(calcularAreaCirculo(4)) == 50.27
assert(calcularAreaCirculo(10)) == 314.16
assert(calcularAreaCirculo(24)) == 1809.56
