'''
4. Write a Python program that accepts two integers 
(n and i) and computes the value of n + nn + nnn +....
'''


''' *************************************************************************************** '''        
''' ************************************** FUNCTIONS ************************************** '''    
''' *************************************************************************************** ''' 

''' ========================== Function exercise4 (n, i) ======================== '''
def exercise4 (n, i) :
    
    numberStr = "" 
    total = 0 
    counter = 0
    
    
    while (counter < i) :
        numberStr += str(n)
        total += int(numberStr)
        counter += 1
        
    
    return (total)



''' *************************************************************************************** '''        
''' *************************************** MAINAPP *************************************** '''    
''' *************************************************************************************** '''

assert(exercise4(1, 5)) == 12345
assert(exercise4(2, 3)) == 246
assert(exercise4(4, 3))  == 492
